# Task - Compare the values of a and b using the following comparison 
# operators: <, >, <=, >=, ==, and !=. Print the results of each comparison.

a = 10
b = 5

less_than = a < b               # checks if a is less than b
greater_than = a > b            # checks if a is greater than b
less_than_or_equal = a <= b     # checks if a is less than or equal to b
greater_than_or_equal = a >= b  # checks if a is greater than or equal to b
equal = a == b                  # checks if a is equal to b
not_equal = a != b              # checks if a is not equal to b

print("a < b:", less_than)
print("a > b:", greater_than)
print("a <= b:", less_than_or_equal)
print("a >= b:", greater_than_or_equal)
print("a == b:", equal)
print("a != b:", not_equal)

    
    
