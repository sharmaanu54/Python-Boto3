def add(n1, n2): 
    addition = n1+n2 
    return addition

def sub(n1, n2): 
    substract = n1-n2 
    return substract 

def multi(n1, n2): 
    multiplication = n1*n2 
    return multiplication

if __name__ == "__main__":
    print(add(5,10)) 
    print(sub(20,5)) 
    print(multi(100,5))
