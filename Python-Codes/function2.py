#  The program is showing how to pass parameter in a function and return 
# output from it

def add(n1, n2):
    sum = n1+n2
    return sum
    
def sub(n1, n2):
    suubtract = n1-n2
    return suubtract 
    
def multi(n1, n2):
    multiplication = n1*n2
    return multiplication 
    
print(add(5,10))
print(sub(20,5))
print(multi(100,5))
     