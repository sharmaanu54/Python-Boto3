def add(n1, n2):
    sum = n1+n2
    return sum
    
    
def sub(n1,n2):
    subtraction = n1-n2
    return subtraction
    
def multi(n1,n2):
    multiplication = n1*n2
    return multiplication  
    
def div(n1,n2):
    division = n1/n2
    return division   
    
print(add(100,110))
print(sub(1000,888))
print(multi(85,10))
print(div(2000000,5000))   