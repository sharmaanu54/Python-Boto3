num1 = 10
num2 = 3

# Integer Division
result1 = num1 // num2
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = num1 % num2
print("Modulus (Remainder):", result2)

# Modulus (Remainder)
num1 = (num1 // num2) * num2 + (num1 % num2)
print(num1)

# Absolute Value
result3 = abs(-7)
print("Absolute Value:", result3)
